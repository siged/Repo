public class TriangleOf55Stars
    {
        public static void main(String[] args)
        {
            int a,b;
            for(a=1; a<=11; a++)
            {
                for(b=1; b<a; b++)
                {
                    System.out.print("*");
                }
                System.out.println();
            }
        }
    }
